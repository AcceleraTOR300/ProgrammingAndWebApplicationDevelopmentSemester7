public class NameMorseE17 {
    public static void main(String[] args) {
// В - .--
        System.out.print(".-- ");
// А - .-
        System.out.print(".- ");
// Д - -..
        System.out.print("-.. ");
// И - ..
        System.out.print(".. ");
// М - --
        System.out.print("--");
        System.out.println();
    }
}
